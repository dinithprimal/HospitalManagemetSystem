    private void jTextField_SearchDoctorNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_SearchDoctorNameKeyReleased
        
        try {
            
            String sql = "SELECT reg_no,firstname FROM doctor WHERE firstname LIKE '%"+jTextField_SearchDoctorName.getText()+"%'";
            pst = (PreparedStatement) con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            DefaultTableModel tm = (DefaultTableModel)jTableSearchDoctorView.getModel();
            tm.setRowCount(0);
            
            int i = 0;
            
            while(rs.next()) {
                
                Object[] o= {rs.getString("reg_no"),rs.getString("firstname")};
                
                tm.addRow(o);
                
                i++;
                
            }
            
            if("".equals(jTextField_SearchDoctorName.getText())){
            
                tm.setRowCount(0);
                i=0;
        
            }
            
            jLabel_SearchDoctorViewRowCount.setText(i+" Row(s) are Selected");
            
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_jTextField_SearchDoctorNameKeyReleased

