    private void jTableSearchDoctorViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSearchDoctorViewMouseClicked
        
        try {
            
            int row = jTableSearchDoctorView.getSelectedRow();
            String reg_no = (jTableSearchDoctorView.getModel().getValueAt(row, 0)).toString();
            
            String sql = "SELECT reg_no,firstname FROM doctor WHERE reg_no = '"+reg_no+"'";
            pst = (PreparedStatement) con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                
                regNo = rs.getString("reg_no");
                Name = rs.getString("firstname");
                 
            }
            Home.jTextFieldAdmissionDoctorName.setText(Name);
            Home.jTextFieldAdmissionDoctorRegNo.setText(regNo);
            
            this.dispose();
            
        } catch (Exception e) {
            System.err.println(e);
        }
        
    }//GEN-LAST:event_jTableSearchDoctorViewMouseClicked

